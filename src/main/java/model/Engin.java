package model;

/**
 *
 * @author TS1SIO
 */
public class Engin {


    private int id;
    private String type ;
    private String numOrdre ;
    private int caserne ;
   

    public Engin() {
       
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
    public String getNumOrdre() {
        return numOrdre;
    }

    public void setNumOrdre(String numOrdre) {
        this.numOrdre = numOrdre;
    }
    
    public int getCaserne() {
        return caserne;
    }

    public void setCaserne(int Caserne) {
        this.caserne = caserne;
    }
    


}
